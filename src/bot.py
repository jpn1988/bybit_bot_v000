    async def start(self) -> None:
        """D√©marre le bot de mani√®re asynchrone."""
        self.logger.info("üöÄ D√©marrage du bot Bybit (mode asynchrone, testnet: {})...", 
                        self.orchestrator.testnet)
        try:
            # Configurer le signal handler pour l'arr√™t propre (Windows compatible)
            self._setup_signal_handlers()

            await self.orchestrator.start()
        except asyncio.CancelledError:
            self.logger.info("‚èπÔ∏è Bot arr√™t√© par annulation de t√¢che (uptime: {:.1f}s)", 
                            time.time() - self.orchestrator.start_time)
        except Exception as e:
            self.logger.error("‚ùå Erreur critique dans le runner asynchrone: {} (type: {})", 
                             str(e), type(e).__name__, exc_info=True)
        finally:
            self.logger.info("üèÅ Bot Bybit arr√™t√© (session termin√©e)")

    def _setup_signal_handlers(self) -> None:
        """
        Configure les signal handlers pour l'arr√™t propre.
        
        Cette m√©thode configure les handlers pour SIGINT (Ctrl+C) et SIGTERM
        pour permettre un arr√™t propre du bot. Sur Windows, les signal handlers
        ne sont pas support√©s par asyncio.
        
        Side effects:
            - Configure les handlers de signal si support√©s
            - Log un avertissement si non support√©s
        """
        try:
            loop = asyncio.get_running_loop()
            for sig in (signal.SIGINT, signal.SIGTERM):
                loop.add_signal_handler(
                    sig, lambda: asyncio.create_task(self.stop())
                )
        except NotImplementedError:
            # Sur Windows, les signal handlers ne sont pas support√©s
            self.logger.debug(
                "Signal handlers non support√©s sur cette plateforme"
            )

    async def stop(self):
        """Arr√™te le bot de mani√®re asynchrone."""
        if self.running:
            self.running = False
            self.logger.info("Signal d'arr√™t re√ßu, arr√™t propre du bot...")
            # Utiliser la nouvelle m√©thode stop() du orchestrateur refactoris√©
            # Utiliser await car stop() est maintenant asynchrone
            await self.orchestrator.stop()
            # Annuler toutes les t√¢ches restantes pour permettre l'arr√™t de l'event loop
            await self._cancel_remaining_tasks()
            self.logger.info("Toutes les t√¢ches asynchrones ont √©t√© annul√©es.")
            sys.exit(0)  # Forcer la sortie apr√®s l'arr√™t propre

    async def _cancel_remaining_tasks(self) -> None:
        """
        Annule toutes les t√¢ches asyncio restantes.
        
        Cette m√©thode annule toutes les t√¢ches asyncio en cours d'ex√©cution
        pour permettre un arr√™t propre de l'event loop. Elle est appel√©e
        lors de l'arr√™t du bot pour √©viter les t√¢ches zombies.
        
        Side effects:
            - Annule toutes les t√¢ches asyncio
            - Attend la fin de l'annulation
            - Permet l'arr√™t propre de l'event loop
        """
        tasks = [
            t
            for t in asyncio.all_tasks()
            if t is not asyncio.current_task()
        ]
        for task in tasks:
            task.cancel()
        await asyncio.gather(*tasks, return_exceptions=True)


